
// Assignment Operators
var c = b;
c += 2;
c -= 2; // c = c - 2;
c *= 2;
c /= 2;
console.log(c);

// Comparison Operators
var x = 34;
var y = 56;
console.log(x == y);
console.log(x >= y);
console.log(x <= y);
console.log(x > y);
console.log(x < y);

// Logical Operators

// Logical and
console.log(true && true)
console.log(true && false)
console.log(false && true)
console.log(false && false)

// Logical or
console.log(true || true)
console.log(true || false)
console.log(false || true)
console.log(false || false)

// Logical not
console.log(!false);
console.log(!true);
